let show_unop = function
  | Cop.Onotbool -> "notbool"
  | Cop.Onotint  -> "notint"
  | Cop.Oneg     -> "neg"
  | Cop.Osha_1   -> "sha_1"

let show_binop = function
  | Cop.Oadd -> "add"
  | Cop.Osub -> "sub"
  | Cop.Omul -> "mul"
  | Cop.Odiv -> "div"
  | Cop.Omod -> "mod"
  | Cop.Oexp -> "exp"
  | Cop.Oand -> "and"
  | Cop.Oor  -> "or"
  | Cop.Oxor -> "xor"
  | Cop.Oshl -> "shl"
  | Cop.Oshr -> "shr"
  | Cop.Oeq  -> "eq"
  | Cop.One  -> "ne"
  | Cop.Olt  -> "lt"
  | Cop.Ogt  -> "gt"
  | Cop.Ole  -> "le"
  | Cop.Oge  -> "ge"
  | Cop.Osha_2 -> "sha_2"                 
